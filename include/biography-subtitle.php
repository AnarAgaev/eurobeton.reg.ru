<p><b>Гочелашвили Муртаз Александрович</b> родился 22 июня 1973 года, окончил Санкт-Петербургский
    государственный горный университет имени Плеханова (горный инженер). Получил степень МВА в
    Российской академии госслужбы при Президенте РФ (Антикризисное управление) и диплом
    бизнес-управления в Германской академии менеджмента в Нижней Саксонии (DMAN) по направлению
    «Стратегический и проектный менеджмент» (направление «Глобальные финансы, инвестиции и
    логистика»)
</p>
